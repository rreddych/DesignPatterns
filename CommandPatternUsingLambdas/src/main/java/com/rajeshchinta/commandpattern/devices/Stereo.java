package com.rajeshchinta.commandpattern.devices;

public class Stereo {
	
	public void on() {
		System.out.println("Stereo turned on");
	}
	
	public void setCD() {
		System.out.println("Stereo CD set");
	}
	
	public void setVolume() {
		System.out.println("Stereo volume set to 10");
	}
	
	public void off() {
		System.out.println("Stereo turned off");
	}

}
