package com.rajeshchinta.commandpattern.devices;

public class CeilingLight {
	public void on() {
		System.out.println("Ceiling Light turned on.");
	}
	
	public void off() {
		System.out.println("Ceiling Light turned off.");
	}
	
	public void dim() {
		System.out.println("Ceiling Light dimmed.");
	}

}
