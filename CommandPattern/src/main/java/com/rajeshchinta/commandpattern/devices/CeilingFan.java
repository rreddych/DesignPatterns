package com.rajeshchinta.commandpattern.devices;

public class CeilingFan{
	public void on() {
		System.out.println("Ceiling Fan turned on.");
	}
	
	public void off() {
		System.out.println("Ceiling Fan turned off.");
	}
	
	public void high() {
		System.out.println("Ceiling Fan on high speed.");
	}
	
	public void medium() {
		System.out.println("Ceiling Fan on medium speed.");
	}
	
	public void low() {
		System.out.println("Ceiling Fan on low speed.");
	}

}
